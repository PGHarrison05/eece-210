num1= float(input("Please Give me a number: "))
num2= float(input("Please Give me another number: "))

operation= input("Please Give me an operation from +, -, *")

if operation== "+":
    print("The sum of your two numbers", num1+num2)

if operation== "-":
    print("The differnece of the two numbers is", num1-num2)

if operation== "*":
    print("The product of the two numbers is", num1*num2)
